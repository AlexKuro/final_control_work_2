package org.example;

public class PackAnimals {
    String birthday;
    String breed;
    String classAn;
    String command;
    long date;
    String other;
    String sex;
    long stamp_number;
    String type;

    public PackAnimals(String birthday, String breed,
                       String classAn, String command,
                       long date, String other,
                       String sex, long stamp_number,
                       String type) {
        this.birthday = birthday;
        this.breed = breed;
        this.classAn = classAn;
        this.command = command;
        this.date = date;
        this.other = other;
        this.sex = sex;
        this.stamp_number = stamp_number;
        this.type = type;
    }
}
